-- Detect nix-config directory location
local function get_nix_config_path()
  local mac_path = vim.env.HOME .. "/home/nix-config"
  local pc_path = vim.env.HOME .. "/nix-config"

  if vim.fn.isdirectory(mac_path) == 1 then
    return mac_path
  else
    return pc_path
  end
end

